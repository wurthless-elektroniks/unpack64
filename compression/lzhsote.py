LZHSOTE_N        = 4096 # buffer size
LZHSOTE_F        = 60   # lookahead buffer size
THRESHOLD        = 2
LZHSOTE_N_CHAR   = (256 - THRESHOLD + LZHSOTE_F) # = 314
LZHSOTE_T        = (LZHSOTE_N_CHAR * 2 - 1)
LZHSOTE_R        = (LZHSOTE_T - 1)
LZHSOTE_MAX_FREQ = 0x8000

class LzhSoteState:
    def __init__(self):
        # this variant works on u32s rather than shorts
        # (code uses lw opcode to access arrays)
        self._freq = [0x00000000] * LZHSOTE_N_CHAR
        self._son  = [0x00000000] * LZHSOTE_T
        self._prnt = [0x00000000] * (LZHSOTE_T + LZHSOTE_N_CHAR)

        # this bit is identical to the original code
        for i in range(LZHSOTE_N_CHAR):
            self._freq[i]            = 1
            self._son[i]             = i + LZHSOTE_T
            self._prnt[i+LZHSOTE_T]  = i

        # this next loop was unrolled by the compiler,
        # but it looks the same as the original code
        i = 0
        j = LZHSOTE_N_CHAR
        while j < LZHSOTE_R:
            self._freq[j] = self._freq[i] + self._freq[i + 1]
            self._son[j]  = i
            self._prnt[i] = self._prnt[i + 1] = j

            i += 2
            j += 1
        
        # same as original
        self._freq[LZHSOTE_T] = 0xFFFF
        self._prnt[LZHSOTE_R] = 0

        # this bit's different, 80001c94 and 80001c90 are both cleared to zero
        self._getbuf = 0
        self._getlen = 0

    def _reconstruct_tree(self):
        j = 0
        for i in range(LZHSOTE_T):
            if self._son[i] >= LZHSOTE_T:
                self._freq[j] = (self._freq[i] + 1) / 2
                self._son[j]  = self._son[i]
                j += 1

        # TODO: do tree construction loop

        # connecting parent/son combinations is unrolled in the disassembly
        # but otherwise identical to the original
        for i in range(LZHSOTE_T):
            k = self._son[i]
            if k >= LZHSOTE_T:
                self._prnt[k] = i
            else:
                self._prnt[k] = self._prnt[k + 1] = i

    def _update_trees(self, value):
        if self._freq[LZHSOTE_R] == LZHSOTE_MAX_FREQ:
            self._reconstruct_tree()

    def decode_char(self):
        # basically DecodeChar, GetBit, etc. inlined with each other

        uVar8 = self._son[LZHSOTE_T]
        while uVar8 <= LZHSOTE_T:
            bit = self._getbuf
            if bit <= 8:
                byte = self._input[self._input_pointer]
                self._input_pointer += 1
                self._getlen += 8
                self._getbuf |= (byte << (8 - bit & 0x1F))
            self._getlen -= 1
            uVar3 = self._getbuf >> 15
            self._getbuf <<= 1

            uVar8 = self._son[uVar8 + (uVar3 & 1)]
        
        iVar7 = uVar8 - LZHSOTE_T
        self._update_trees(iVar7)

        return iVar7
    
